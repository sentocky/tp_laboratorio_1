#ifndef CALCULOS_H_
#define CALCULOS_H_

/** \brief funcion sumar de x+y
*
* \param primer numero ingresado
* \param segundo numero ingresado
* \return resultado de la suma
*
*/

int sumar(int x,int y);

/** \brief funcion restar de x-y
*
* \param primer numero ingresado
* \param segundo numero ingresado
* \return resultado de la resta
*
*/

int restar(int x,int  y);

/** \brief funcion dividir de x/y
*
* \param primer numero ingresado
* \param segundo numero ingresado
* \return resultado de la division
*
*/

float dividir (int x,int y);

/** \brief funcion multiplicar de x*y
*
* \param primer numero ingresado
* \param segundo numero ingresado
* \return resultado de la multiplicacion
*
*/

int multiplicar(int x,int y);

/** \brief funcion factorial de x
*
* \param  numero ingresado
* \return resultado de factorial
*
*/

int factorialA (int x);

/** \brief funcion factorial de y
*
* \param  numero ingresado
* \return resultado de factorial
*
*/

int factorialB (int y);


#endif /* CALCULOS_H_ */
